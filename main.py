def add(num1, num2):
    return num1 + num2

def subtract(num1, num2):
    return num1 - num2

def multiply(num1, num2):
    return num1 * num2

def divide(num1, num2):
    return num1/num2

def main():
    print(add(3, 7))
    print(subtract(10, 7))
    print(multiply(3, 5))
    print(divide(40, 8))

main()